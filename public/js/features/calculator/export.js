// ========== EXPORT TO PDF ==========
async function exportPDF() {
    if (!currentResults) {
        alert('No calculation results to export');
        return;
    }

    try {
        // Create a new jsPDF instance
        const { jsPDF } = window.jspdf;
        const doc = new jsPDF();

        const calc = currentResults.calculations;
        let yPos = 20;

        // Title
        doc.setFontSize(20);
        doc.setFont(undefined, 'bold');
        doc.text('Transformer Design Report', 105, yPos, { align: 'center' });

        yPos += 10;
        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text('Generated by Transformer Calculator v2.0.0', 105, yPos, { align: 'center' });
        doc.text(new Date().toLocaleString(), 105, yPos + 5, { align: 'center' });

        yPos += 15;

        // Input Parameters
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Input Parameters', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        const inputs = currentResults.inputs;
        doc.text(`MVA Rating: ${inputs.mva} MVA`, 14, yPos);
        doc.text(`HV Voltage: ${inputs.hv} kV`, 110, yPos);
        yPos += 5;
        doc.text(`LV Voltage: ${inputs.lv} kV`, 14, yPos);
        doc.text(`Frequency: ${inputs.frequency} Hz`, 110, yPos);
        yPos += 5;
        doc.text(`Vector Group: ${inputs.vectorGroup}`, 14, yPos);
        doc.text(`Cooling: ${inputs.cooling}`, 110, yPos);
        yPos += 10;

        // Summary Results
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Summary Results', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text(`Efficiency: ${calc.losses.efficiency.toFixed(2)}%`, 14, yPos);
        doc.text(`Total Loss: ${calc.losses.totalLoss.toFixed(1)} kW`, 110, yPos);
        yPos += 5;
        doc.text(`Hot Spot: ${calc.temperature.hotSpot.toFixed(1)}°C`, 14, yPos);
        doc.text(`Total Weight: ${calc.dimensions.weights.total.toLocaleString()} kg`, 110, yPos);
        yPos += 10;

        // Electrical Results
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Electrical Results', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text(`HV Current: ${calc.currents.hvCurrent.toFixed(2)} A`, 14, yPos);
        doc.text(`LV Current: ${calc.currents.lvCurrent.toFixed(2)} A`, 110, yPos);
        yPos += 5;
        doc.text(`Core Loss: ${calc.losses.coreLoss.toFixed(2)} kW`, 14, yPos);
        doc.text(`Copper Loss: ${calc.losses.copperLoss.total.toFixed(2)} kW`, 110, yPos);
        yPos += 5;
        doc.text(`Impedance: ${calc.impedance.calculated.toFixed(2)}%`, 14, yPos);
        doc.text(`Regulation: ${calc.impedance.regulation.atFullLoad.toFixed(2)}%`, 110, yPos);
        yPos += 10;

        // Thermal Results
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Thermal Results', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text(`Oil Rise: ${calc.temperature.oilRise.toFixed(1)}°C`, 14, yPos);
        doc.text(`HV Winding Rise: ${calc.temperature.windingRise.hv.toFixed(1)}°C`, 110, yPos);
        yPos += 5;
        doc.text(`LV Winding Rise: ${calc.temperature.windingRise.lv.toFixed(1)}°C`, 14, yPos);
        doc.text(`Hot Spot: ${calc.temperature.hotSpot.toFixed(1)}°C`, 110, yPos);
        yPos += 10;

        // Mechanical Results
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Short Circuit Results', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text(`SC Current: ${calc.shortCircuit.scCurrent.symmetrical.toLocaleString()} A`, 14, yPos);
        doc.text(`Radial Force: ${calc.shortCircuit.forces.radial.toFixed(1)} kN`, 110, yPos);
        yPos += 5;
        doc.text(`Axial Force: ${calc.shortCircuit.forces.axial.toFixed(1)} kN`, 14, yPos);
        doc.text(`Thermal Withstand: ${calc.shortCircuit.thermalWithstand.status}`, 110, yPos);
        yPos += 10;

        // Dimensions
        doc.setFontSize(14);
        doc.setFont(undefined, 'bold');
        doc.text('Dimensions & Weight', 14, yPos);
        yPos += 7;

        doc.setFontSize(10);
        doc.setFont(undefined, 'normal');
        doc.text(`Tank: ${calc.dimensions.tank.length} x ${calc.dimensions.tank.width} x ${calc.dimensions.tank.height} mm`, 14, yPos);
        yPos += 5;
        doc.text(`Core Weight: ${calc.dimensions.weights.core.toLocaleString()} kg`, 14, yPos);
        doc.text(`Winding Weight: ${calc.dimensions.weights.windings.toLocaleString()} kg`, 110, yPos);
        yPos += 5;
        doc.text(`Total Weight: ${calc.dimensions.weights.total.toLocaleString()} kg`, 14, yPos);

        // Footer
        doc.setFontSize(8);
        doc.setFont(undefined, 'italic');
        doc.text('Standards: IEC 60076, IEEE C57, IS 2026', 105, 285, { align: 'center' });

        // Save PDF
        doc.save(`Transformer_Design_${inputs.mva}MVA_${new Date().getTime()}.pdf`);

        alert('PDF exported successfully!');

    } catch (error) {
        console.error('PDF export error:', error);
        alert('Error exporting PDF. Please ensure jsPDF library is loaded.');
    }
}

// ========== EXPORT TO EXCEL ==========
function exportExcel() {
    if (!currentResults) {
        alert('No calculation results to export');
        return;
    }

    try {
        const calc = currentResults.calculations;
        const inputs = currentResults.inputs;

        // Create workbook
        const wb = XLSX.utils.book_new();

        // Sheet 1: Input Parameters
        const inputData = [
            ['Parameter', 'Value', 'Unit'],
            ['MVA Rating', inputs.mva, 'MVA'],
            ['HV Voltage', inputs.hv, 'kV'],
            ['LV Voltage', inputs.lv, 'kV'],
            ['Frequency', inputs.frequency, 'Hz'],
            ['Phases', inputs.phases, ''],
            ['Vector Group', inputs.vectorGroup, ''],
            ['Cooling Type', inputs.cooling, ''],
            ['Core Material', inputs.coreMaterial, ''],
            ['Winding Material', inputs.windingMaterial, ''],
            ['Flux Density', inputs.fluxDensity, 'T'],
            ['Volts per Turn', inputs.voltsPerTurn, 'V'],
            ['Current Density', inputs.currentDensity, 'A/mm²'],
            ['Impedance', inputs.impedance, '%'],
            ['Ambient Temp', inputs.ambientTemp, '°C'],
            ['Altitude', inputs.altitude, 'm']
        ];
        const ws1 = XLSX.utils.aoa_to_sheet(inputData);
        XLSX.utils.book_append_sheet(wb, ws1, 'Input Parameters');

        // Sheet 2: Summary Results
        const summaryData = [
            ['Parameter', 'Value', 'Unit'],
            ['Efficiency', calc.losses.efficiency.toFixed(3), '%'],
            ['Total Loss', calc.losses.totalLoss.toFixed(2), 'kW'],
            ['Hot Spot Temperature', calc.temperature.hotSpot.toFixed(1), '°C'],
            ['Total Weight', calc.dimensions.weights.total, 'kg']
        ];
        const ws2 = XLSX.utils.aoa_to_sheet(summaryData);
        XLSX.utils.book_append_sheet(wb, ws2, 'Summary');

        // Sheet 3: Electrical Results
        const electricalData = [
            ['Parameter', 'Value', 'Unit'],
            ['HV Current', calc.currents.hvCurrent.toFixed(2), 'A'],
            ['LV Current', calc.currents.lvCurrent.toFixed(2), 'A'],
            ['Core Loss', calc.losses.coreLoss.toFixed(2), 'kW'],
            ['Copper Loss (HV)', calc.losses.copperLoss.hv.toFixed(2), 'kW'],
            ['Copper Loss (LV)', calc.losses.copperLoss.lv.toFixed(2), 'kW'],
            ['Eddy Loss', calc.losses.eddyLoss.toFixed(2), 'kW'],
            ['Stray Loss', calc.losses.strayLoss.toFixed(2), 'kW'],
            ['Total Loss', calc.losses.totalLoss.toFixed(2), 'kW'],
            ['Impedance (Calculated)', calc.impedance.calculated.toFixed(2), '%'],
            ['Impedance (Target)', calc.impedance.target.toFixed(2), '%'],
            ['Voltage Regulation', calc.impedance.regulation.atFullLoad.toFixed(2), '%']
        ];
        const ws3 = XLSX.utils.aoa_to_sheet(electricalData);
        XLSX.utils.book_append_sheet(wb, ws3, 'Electrical');

        // Sheet 4: Thermal Results
        const thermalData = [
            ['Parameter', 'Value', 'Unit'],
            ['Ambient Temperature', calc.temperature.temperatures.ambient.toFixed(1), '°C'],
            ['Oil Rise', calc.temperature.oilRise.toFixed(1), '°C'],
            ['Top Oil Temperature', calc.temperature.temperatures.topOil.toFixed(1), '°C'],
            ['HV Winding Rise', calc.temperature.windingRise.hv.toFixed(1), '°C'],
            ['LV Winding Rise', calc.temperature.windingRise.lv.toFixed(1), '°C'],
            ['Hot Spot Temperature', calc.temperature.hotSpot.toFixed(1), '°C'],
            ['IEC Compliance', calc.temperature.iecCompliance.overall, '']
        ];
        const ws4 = XLSX.utils.aoa_to_sheet(thermalData);
        XLSX.utils.book_append_sheet(wb, ws4, 'Thermal');

        // Sheet 5: Mechanical Results
        const mechanicalData = [
            ['Parameter', 'Value', 'Unit'],
            ['SC Current (Symmetrical)', calc.shortCircuit.scCurrent.symmetrical, 'A'],
            ['SC Current (Peak)', calc.shortCircuit.scCurrent.asymmetrical, 'A'],
            ['Radial Force', calc.shortCircuit.forces.radial.toFixed(1), 'kN'],
            ['Axial Force', calc.shortCircuit.forces.axial.toFixed(1), 'kN'],
            ['Radial Stress', calc.shortCircuit.stresses.radial.toFixed(1), 'MPa'],
            ['Axial Stress', calc.shortCircuit.stresses.axial.toFixed(1), 'MPa'],
            ['Thermal Withstand', calc.shortCircuit.thermalWithstand.status, ''],
            ['Mechanical Withstand', calc.shortCircuit.mechanicalWithstand.status, '']
        ];
        const ws5 = XLSX.utils.aoa_to_sheet(mechanicalData);
        XLSX.utils.book_append_sheet(wb, ws5, 'Short Circuit');

        // Sheet 6: Dimensions & Weight
        const dimensionsData = [
            ['Parameter', 'Value', 'Unit'],
            ['Tank Length', calc.dimensions.tank.length, 'mm'],
            ['Tank Width', calc.dimensions.tank.width, 'mm'],
            ['Tank Height', calc.dimensions.tank.height, 'mm'],
            ['Oil Volume', calc.dimensions.oil.volume, 'liters'],
            ['Core Weight', calc.dimensions.weights.core, 'kg'],
            ['Winding Weight', calc.dimensions.weights.windings, 'kg'],
            ['Tank Weight', calc.dimensions.weights.tank, 'kg'],
            ['Oil Weight', calc.dimensions.weights.oil, 'kg'],
            ['Accessories Weight', calc.dimensions.weights.accessories, 'kg'],
            ['Total Weight', calc.dimensions.weights.total, 'kg']
        ];
        const ws6 = XLSX.utils.aoa_to_sheet(dimensionsData);
        XLSX.utils.book_append_sheet(wb, ws6, 'Dimensions');

        // Save Excel file
        XLSX.writeFile(wb, `Transformer_Design_${inputs.mva}MVA_${new Date().getTime()}.xlsx`);

        alert('Excel file exported successfully!');

    } catch (error) {
        console.error('Excel export error:', error);
        alert('Error exporting Excel. Please ensure SheetJS library is loaded.');
    }
}

// Export functions for external use
window.exportPDF = exportPDF;
window.exportExcel = exportExcel;
